// --- State ---
const menuEl = document.getElementById('menu');
const artEl = document.getElementById('ascii-art');
const factEl = document.getElementById('fun-fact');
const drawPanel = document.getElementById('draw-panel');
const drawGrid = document.getElementById('draw-grid');
const galleryView = document.getElementById('gallery-view');

let currentAnimal = null;

// --- Draw Your Own state ---
const GRID_ROWS = 16;
const GRID_COLS = 32;
let grid = [];
let isDrawing = false;
let currentChar = '*';
let drawColor = '#4fc3f7';

// --- Menu ---
Object.keys(artGallery).forEach((name, index) => {
    const item = document.createElement('div');
    item.className = 'menu-item' + (index === 0 ? ' active' : '');
    item.textContent = `🎨 ${name}`;
    item.onclick = () => selectArt(name, item);
    menuEl.appendChild(item);
});

// Draw Your Own menu item
const drawMenuItem = document.createElement('div');
drawMenuItem.className = 'menu-item draw-menu-item';
drawMenuItem.textContent = '✏️ Draw Your Own!';
drawMenuItem.onclick = () => showDrawMode(drawMenuItem);
menuEl.appendChild(drawMenuItem);

// --- Gallery ---
function selectArt(name, element) {
    hideDrawMode();

    document.querySelectorAll('.menu-item').forEach(item => {
        item.classList.remove('active');
    });
    element.classList.add('active');

    currentAnimal = name;
    const selected = artGallery[name];
    artEl.textContent = selected.art;
    artEl.className = `ascii-art color-${selected.color}`;

    // Random fun fact
    const fact = selected.funFacts[Math.floor(Math.random() * selected.funFacts.length)];
    factEl.textContent = `💡 ${fact}`;
    factEl.style.display = 'block';

    galleryView.style.display = 'flex';
    drawPanel.style.display = 'none';
}

// --- Draw Your Own ---
function showDrawMode(element) {
    document.querySelectorAll('.menu-item').forEach(item => {
        item.classList.remove('active');
    });
    element.classList.add('active');

    currentAnimal = null;
    galleryView.style.display = 'none';
    drawPanel.style.display = 'flex';
    factEl.style.display = 'none';

    initGrid();
}

function hideDrawMode() {
    drawPanel.style.display = 'none';
    galleryView.style.display = 'flex';
}

function initGrid() {
    drawGrid.innerHTML = '';
    grid = [];
    for (let r = 0; r < GRID_ROWS; r++) {
        grid[r] = [];
        for (let c = 0; c < GRID_COLS; c++) {
            grid[r][c] = ' ';
            const cell = document.createElement('span');
            cell.className = 'grid-cell';
            cell.dataset.row = r;
            cell.dataset.col = c;
            cell.textContent = ' ';
            cell.addEventListener('mousedown', onCellMouseDown);
            cell.addEventListener('mouseenter', onCellMouseEnter);
            drawGrid.appendChild(cell);
        }
        drawGrid.appendChild(document.createTextNode('\n'));
    }
}

function onCellMouseDown(e) {
    e.preventDefault();
    isDrawing = true;
    paintCell(e.target);
}

function onCellMouseEnter(e) {
    if (isDrawing) {
        paintCell(e.target);
    }
}

document.addEventListener('mouseup', () => {
    isDrawing = false;
});

function paintCell(cell) {
    const r = parseInt(cell.dataset.row);
    const c = parseInt(cell.dataset.col);
    if (isNaN(r) || isNaN(c)) return;
    grid[r][c] = currentChar;
    cell.textContent = currentChar;
    cell.style.color = drawColor;
}

// Char picker
document.querySelectorAll('.char-btn').forEach(btn => {
    btn.addEventListener('click', () => {
        document.querySelectorAll('.char-btn').forEach(b => b.classList.remove('selected'));
        btn.classList.add('selected');
        currentChar = btn.dataset.char;
    });
});

// Color picker
document.querySelectorAll('.draw-color-btn').forEach(btn => {
    btn.addEventListener('click', () => {
        document.querySelectorAll('.draw-color-btn').forEach(b => b.classList.remove('selected'));
        btn.classList.add('selected');
        drawColor = btn.dataset.color;
    });
});

// Clear grid
document.getElementById('clear-grid').addEventListener('click', () => {
    initGrid();
});

// Copy art to clipboard
document.getElementById('copy-art').addEventListener('click', () => {
    const text = grid.map(row => row.join('')).map(line => line.trimEnd()).join('\n');
    navigator.clipboard.writeText(text).then(() => {
        const btn = document.getElementById('copy-art');
        const original = btn.textContent;
        btn.textContent = 'Copied!';
        setTimeout(() => { btn.textContent = original; }, 1500);
    });
});

